//Different calculator methods added for the sake of practice
const calculator = {

    add: function (num1, num2) {
        let result = num1 + num2;
        return result;
    },

    subtract(num1, num2){
        let result = num1 - num2;
        return result;
    },

    multiply: (num1, num2) => {
        let result = num1 * num2;
        return result;
    },

    divide: function (num1, num2) {
        let result = num1 / num2;
        return result;
    }


}

export default calculator;